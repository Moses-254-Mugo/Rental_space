function login(){
    document.getElementById("signIN").style.display = "block"
    document.getElementById("signUP").style.display = "none"
}

function signup(){
    document.getElementById("signUP").style.display = "block"
    document.getElementById("signIN").style.display = "none"
}

function hideForm(){
    document.getElementById("signIN").style.display = "none"
    document.getElementById("signUP").style.display = "none"
}